package trust_management.sockets_pruebas;
import java.io.*;
import java.net.*;

public class cliente_socket {

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // MAIN
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public static void main(String[] args) {
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // VARIABLES
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        final String host = "localhost";
        final int puerto = 12345;
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // SOCKETS
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        try (Socket cliente = new Socket(host, puerto);
             BufferedReader entrada = new BufferedReader(new InputStreamReader(cliente.getInputStream()));
             PrintWriter salida = new PrintWriter(cliente.getOutputStream(), true);
             BufferedReader entradaUsuario = new BufferedReader(new InputStreamReader(System.in))) {

            System.out.println("Conectado al servidor en " + host + ":" + puerto);

            // Enviar mensaje al servidor
            System.out.print("Ingrese un mensaje para el servidor: ");
            String mensajeUsuario = entradaUsuario.readLine();
            salida.println(mensajeUsuario);

            // Recibir respuesta del servidor
            String respuestaServidor = entrada.readLine();
            System.out.println("Respuesta del servidor: " + respuestaServidor);
            // Recibir respuesta del servidor
            String respuestaServidor2 = entrada.readLine();
            System.out.println("Respuesta del servidor: " + respuestaServidor2);

        } catch (IOException e) {
            e.printStackTrace();
        }
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    }
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}

